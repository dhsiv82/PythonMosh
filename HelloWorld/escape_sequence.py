'''
Escape and Sequences With Mosh \
'''

# \" add a " in string
# \' add a ' in string
# \\ add a \ in string
# \n new line

# \ allows you to escape the quotes in a string and add more quotes inside
# \" is an escape sequence

# with the \ it allows you to print another " inside of a string
course = "Python \"Programming"
print(course)

course2 = "Python \'Programming"
print(course2)

course3 = "Python \\Programming"
print(course3)

course4 = "Python \nProgramming"
print(course4)
