'''
Learning Strings With Mosh
'''

course = "Python Programming"

print(len(course))

print(course[0])  # 0 is first
print(course[-1])  # takes back to first char at end of string

print(course[0:3])  # print string first second and third

print(course[:3])  # everything up to 3rd char
print(course[:])  # prints every character
